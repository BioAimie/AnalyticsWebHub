SET NOCOUNT ON
SELECT 
CASE WHEN [ItemID] IN ('RFIT-ASY-0105','RFIT-ASY-0124','RFIT-ASY-0107','RFIT-ASY-0125') THEN 'RP'
WHEN [ItemID] IN ('RFIT-ASY-0114','RFIT-ASY-0126','RFIT-ASY-0109','RFIT-ASY-0127') THEN 'BCID'
WHEN [ItemID] IN ('RFIT-ASY-0116','RFIT-ASY-0104') THEN 'GI'
WHEN [ItemID] IN ('RFIT-ASY-0118','RFIT-ASY-0119') THEN 'ME'
END AS Panel
,[QtyShipped]
,IIF(ShipDate > GETDATE()-30,[QtyShipped],0) AS [30Days]
,IIF(ShipDate > GETDATE()-30,'30Day','Total') AS [Note]
INTO #pouchShip
FROM [PMS1].[dbo].[vPouchShipmentsWithAnnotations_IOID]
WHERE ShipDate > '2016-01-01'
AND [ItemID] IN ('RFIT-ASY-0105','RFIT-ASY-0124','RFIT-ASY-0107','RFIT-ASY-0125'
,'RFIT-ASY-0114','RFIT-ASY-0126','RFIT-ASY-0109','RFIT-ASY-0127'
,'RFIT-ASY-0116','RFIT-ASY-0104', 'RFIT-ASY-0118','RFIT-ASY-0119')
 
SELECT Panel, SUM(QtyShipped) AS PouchesShipped, SUM([30Days]) AS [Last30Days], [Note]
FROM #pouchShip
GROUP BY Panel, [Note]
ORDER BY Panel, Note

DROP TABLE #pouchShip